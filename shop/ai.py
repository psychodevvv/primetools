def classify(name):
    """Определяет категорию товара по названию. Возвращает название или None."""
    text = ' ' + (name or '').lower().replace('ё', 'е') + ' '
    for category, keywords in CATEGORY_RULES:
        for keyword in keywords:
            if keyword.replace('ё', 'е') in text:
                return category
    return None
